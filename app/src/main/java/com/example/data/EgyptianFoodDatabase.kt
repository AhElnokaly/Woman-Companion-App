    fun normalizeText(input: String): String {
        return input.trim()
            .replace("أ", "ا")
            .replace("إ", "ا")
            .replace("آ", "ا")
            .replace("ة", "ه")
            .replace("ى", "ي")
            .replace("ئ", "ي")
            .replace("ئ", "ي")
            .replace("ء", "ا")
            .replace("ؤ", "و")
    }

    /**
     * Parse food names from user text, matching keywords of predefined Egyptian foods.
     * Handles sentences like: "اكلت طبق ملوخية و رز و قطعة فراخ" or "شربت قهوة بلبن"
     */
    fun extractFoodsFromInput(userInput: String): List<EgyptianFoodEntity> {
        val normalizedInput = normalizeText(userInput)
        val detected = mutableListOf<EgyptianFoodEntity>()

        for (food in presetFoods) {
            val keywordsList = food.keywords.split(",")
            var matches = false
            for (kw in keywordsList) {
                val normalizedKw = normalizeText(kw)
                if (normalizedKw.isNotEmpty() && normalizedInput.contains(normalizedKw)) {
                    matches = true
                    break
                }
            }
            if (matches) {
                detected.add(food)
            }
        }
        return detected
    }
